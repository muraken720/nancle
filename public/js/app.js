/**
 *  shorthand for getting root compiler
 */
function getRoot (compiler) {
    while (compiler.parent) {
        compiler = compiler.parent
    }
    return compiler
}

/**
 *  for convenience & minification
 */
function defGetSet (obj, key, def) {
    Object.defineProperty(obj, key, def)
}

